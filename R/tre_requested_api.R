.tre_not_implemented <- function(name) {
  stop(sprintf("Function '%s' is not yet implemented in AHRITREC.", name), call. = FALSE)
}

.tre_make_stub <- function(name) {
  force(name)
  function(...) .tre_not_implemented(name)
}

